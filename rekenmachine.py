cijfer1 = int(input("Voer een rekencijfer in: "))

rek = input("Voer uw rekenmethode in: ")

if rek == "=":
    print(cijfer1)

else:
    cijfer2 = int(input("Voer een rekencijfer in: "))

    
if rek == "+":
    print(cijfer1 + cijfer2)

elif rek == "-":
    print(cijfer1 - cijfer2)

elif rek == "/":
    print(cijfer1 / cijfer2)

elif rek == "*":
    print(cijfer1 * cijfer2)
























      


        
        

